// Helper functions
export const getShelterById = (id: string): Shelter | undefined => {
  return shelters.find((s) => s.id === id);
};

export const getSheltersByCity = (city: string): Shelter[] => {
  return shelters.filter((s) => s.address.city.toLowerCase() === city.toLowerCase());
};

export const getSheltersByState = (state: GermanState): Shelter[] => {
  return shelters.filter((s) => s.address.state === state);
};

export const getSheltersByStatus = (status: string): Shelter[] => {
  return shelters.filter((s) => s.status === status);
};

export const getSheltersByType = (type: string): Shelter[] => {
  return shelters.filter((s) => s.type === type);
};

export const getActiveShelters = (): Shelter[] => {
  return shelters.filter((s) => s.status === 'active');
};

export const getPendingApprovals = (): Shelter[] => {
  return shelters.filter((s) => s.approvalStatus === 'pending');
};
